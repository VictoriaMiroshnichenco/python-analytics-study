"""Завдання 7. Географічний аналіз компенсації Python-розробників"""

condition = df["LanguageHaveWorkedWith"].str.contains("python", case=False)

df.filter(items=["Country","ConvertedCompYearly","LanguageHaveWorkedWith"])\
.where(condition)\
.dropna()\
.groupby("Country")\
.agg(
        Mean=("ConvertedCompYearly", "mean"),
        Median=("ConvertedCompYearly", "median")
    )\
.round(2)

