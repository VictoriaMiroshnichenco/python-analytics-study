"""*Завдання 9. Аналіз популярності Python по віковим категоріям"""



df.filter(items=["ResponseId","Age","LanguageHaveWorkedWith"])\
.dropna()\
.assign(is_python=lambda x: x["LanguageHaveWorkedWith"].str.contains("python", case=False))\
.groupby("Age").agg(
        python=("is_python", "sum"),
        total=("ResponseId", "count")
    )\
.assign(Python_percentage=lambda x: round(x["python"] / x["total"] * 100, 2))\
.filter(items=["Age","Python_percentage"])

df["ConvertedCompYearly"].quantile(0.75)

"""*Завдання 10. Аналіз індустрій серед високооплачуваних віддалених працівників"""

